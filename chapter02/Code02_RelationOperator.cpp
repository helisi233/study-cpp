#include <iostream>

using namespace std;

int main(void) {

    int a = 10;
    int b = 20;

    // 返回0-false 返回1-true
    cout << (a == b) << endl;
    cout << (a != b) << endl;
    cout << (a > b) << endl;
    cout << (a < b) << endl;
    cout << (a >= b) << endl;
    cout << (a <= b) << endl;

    return 0;
}